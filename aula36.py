"""
Operadores de atribuição
= += -= *= /= //= **= %=
"""

contador = 10 # variavel contador é criada e recebe o valor inteiro 10
contador /= 5 # /= este é um atalho para a operação contador = contador / 5, a divisão (/) sempre retorna um número de ponto flutuante ex: 2.0
print(contador) # valor atual do contador é impresso