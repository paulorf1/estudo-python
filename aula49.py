"""
for in com listas
"""
lista = ['Maria', 'Helena', 'Luiz'] # cria uma lista chamada lista
for nome in lista: # este é um loop for, em cada iteração do loop, a variável nome receberá um dos elementos da lista, em ordem 
    print(nome, type(nome)) # dentro do loop, esta linha é executada para cada nome na lista, e para cada nome, ela retornará <class 'str'>