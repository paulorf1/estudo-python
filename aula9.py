adicao = 10 + 10 #operador + realiza a adição, e armazena o resultado na variavel adiacao
print('Adição:', adicao) #exibe o resultado da variavel adicao

subtracao = 10 - 5 #operador - realiza a subtração, e armazena o resultado na variavel subtracao
print('Subtração:', subtracao) #exibe o resultado da variavel subtracao

multiplicacao = 10 * 10 #operador * realiza a multiplicação, e armazena o resultado na variavel multiplicacao
print('Multiplicação:', multiplicacao) #exibe o resultado da variavel multiplicacao

divisao = 10 / 3  # float #operador / realiza a divisão verdadeira, o resultado sempre sera um numero de ponto flutuante (float)
print('Divisão:', divisao) #exibe o resultado da variavel divisao

divisao_inteira = 10 // 3 #operador // realiza a divisão inteira e descarta a parte decimal, retornando apenas o valor inteiro
print('Divisão inteira:', divisao_inteira) #exibe o resultado da variavel divisao_inteira

exponenciacao = 2 ** 10 #operador ** realiza a exponenciação, eleva o primeiro número à potência do segundo número
print('Exponenciação:', exponenciacao) #exibe o resultado da variavel exponenciacao

modulo = 55 % 2  #O operador % realiza a operação de móduloo (resto da divisão), retorna o resto da divisão inteira
print('Módulo:', modulo) #exibe o resultado da variavel modulo

print(10 % 8 == 0) #O resto de 10 dividido por 8 é 2, 2 == 0 é False
print(16 % 8 == 0) #O resto de 16 dividido por 8 é 0, 0 == 0 é True
print(10 % 2 == 0) #O resto de 10 dividido por 2 é 0, 0 == 0 é True
print(15 % 2 == 0) #O resto de 15 dividido por 2 é 1, 1 == 0 é False
print(16 % 2 == 0) #O resto de 16 dividido por 2 é 0, 0 == 0 é True