concatenacao = 'A' + 'B' + 'C' #o operador + quando usado entre strings, ele realiza uma concatenação (junta as strings)
print(concatenacao) #exibe o resultado que foi armazenado na variavel concatenacao
a_dez_vezes = 'A' * 10 #o operador * quando usado entre uma string e um número inteiro, ele repete a string o número de vezes especificado
tres_vezes_paulo = 3 * 'Paulo' #da mesma forma, a string 'Paulo' é repetida 3 vezes
print(a_dez_vezes) #exibe o valor da variavel a_dez_vezes
print(tres_vezes_paulo) #exibe o valor da variavel tres_vezes_paulo